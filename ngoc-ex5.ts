let day: number = 8;

switch (day) {
  case 0:
    console.log("Đó là một ngày chủ nhật.");
    break;
  case 1:
    console.log("Đó là một ngày thứ hai.");
    break;
  case 2:
    console.log("Đó là một ngày thứ ba.");
    break;
  case 3:
    console.log("Đó là một ngày thứ tư.");
    break;
  case 4:
    console.log("Đó là một ngày thứ năm.");
    break;
  case 5:
    console.log("Đó là một ngày thứ sáu.");
    break;
  case 6:
    console.log("Đó là một ngày thứ bảy.");
    break;
  default:
    console.log("Không tồn tại ngày ni!");
    break;
}
